/* Popüler araçlar */
export const POPULAR_TOOLS = ALL_TOOLS.filter(t => t.popular);

/* Canlı (çalışır) araçlar */
export const LIVE_TOOLS = ALL_TOOLS.filter(t => t.status === "live");

/* ID ile araç bul */
export function findTool(id: string): Tool | undefined {
  return ALL_TOOLS.find(t => t.id === id);
}

/* Arama */
export function searchTools(query: string): Tool[] {
  const q = query.toLowerCase().trim();
  if (!q) return ALL_TOOLS;
  return ALL_TOOLS.filter(t =>
    t.name.toLowerCase().includes(q) ||
    t.nameEn.toLowerCase().includes(q) ||
    t.description.toLowerCase().includes(q) ||
    t.searchTerms.some(term => term.includes(q)) ||
    t.inputFormats.some(f => f.includes(q)) ||
    (t.outputFormats ?? []).some(f => f.includes(q))
  );
}

